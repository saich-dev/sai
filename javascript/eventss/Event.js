// Event in javascript //
//In JavaScript, an event is an action or occurrence that happens in the browser, such as user interactions, browser actions, or system-generated events.//

//Types of Events in JavaScript//
//1. Mouse Events:These events are triggered by mouse actions. //

//click: Fires when an element is clicked.//

function content() {
    alert('Button clicked!');
  }
  document.getElementById('seef').addEventListener('click',content);
  


//dblclick: Fires when an element is double-clicked.//

function content() {
    alert('Button clicked!');
  }
document.getElementById('myButton').addEventListener('dblclick',content)


  
//mousedown: Fires when a mouse button is pressed down on an element.//
 
function content() {
    alert('Button clicked!');
  }
document.getElementById('myButton').addEventListener('mousedown',content)




//mouseup: Fires when a mouse button is released over an element.//

function content() {
    alert('Button clicked!');
  }
document.getElementById('myButton').addEventListener('mouseup',content)




//mouseover: Fires when the mouse pointer is moved onto an element.//

function content() {
    alert('Button clicked!');
  }
document.getElementById('myButton').addEventListener('mouseup',content)




//mouseout: Fires when the mouse pointer is moved out of an element.//

function content() {
    alert('Button clicked!');
  }
document.getElementById('myButton').addEventListener('mouseup',content)




//mousemove: Fires when the mouse pointer is moving while it is over an element.//

function content() {
    alert('Button clicked!');
  }
document.getElementById('myButton').addEventListener('mouseup',content)





//2. Keyboard Events:keydown: Fires when a key is pressed down.//

