//logical operators//
//AND (&&)//

let x = 5;
let y = 10;

if (x < 10 && y > 10) {
  console.log("True");
} else {
  console.log("False");
}

//OR (||)//

let z = 5;
let w = 10;

if (z < 10 || w > 10) {
  console.log("True");
} 
else {
  console.log("False");
}

//NOT (!)//

let v = 5;

if (!v < 10) {
  console.log("True");
  console.log("False");
}




//Conditional (Ternary) Operator//
var age = 50;
let see = age=50? true : false;
console.log( "see",see);








// Unary Operators://
// Increment (++)//
let aa = 49;
console.log(( ++aa));

// Decrement (--)//
let bb = 30;
console.log(( --bb));

//Unary plus (+)//
let cc = 30;
let dd = 20;
console.log((cc+dd));

//Unary minus (-)//
let e = 56;
let f = 50;
console.log((e-f));


//Bitwise operators//

//Bitwise AND (&)//
let mn = 19;
let ef= 7;
console.log((mn&ef));

//Bitwise OR (|)//
let m = 19;
let o= 7;
console.log((m|o));


//Bitwise XOR (^)//
let me = 35;
let to= 5;
console.log((me^to));

//Bitwise NOT (~)//
let et = 15;
console.log((~et));

//Left shift (<<)//
let mo = 20;
let too= 3;
console.log((mo<<too));

//Right shift (>>)//
let pt= 29;
let tt= 4;
console.log((29 >> 4));

//Zero-fill right shift (>>>)//
let su= 19;
